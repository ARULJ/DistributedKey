    private void jbtndelgrpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtndelgrpActionPerformed
        // TODO add your handling code here:
        String tmp,gid = (String)jcmbgroups.getSelectedItem();
        if(gid==null)
            return;
        try{
        group g=grps.getgroup(gid);
        if(g.getowner().getid().equalsIgnoreCase(me.getid()))
        {
            cmd="DELGRP:"+me.getid()+":"+g.getid();
            s.sendmsg(cmd,false);
            JOptionPane.showMessageDialog(this,"You have deleted the group "+gid+" ..!!","Distributed Key",1);
            for(int i=0;i<jcmbgd.getItemCount();i++) 
                 {
                     tmp=(String)jcmbgd.getItemAt(i);
                     if(tmp.equalsIgnoreCase(gid))
                     {
                         jcmbgd.removeItemAt(i);
                         
                     }
                 }
            for(int i=0;i<jcmbgroups.getItemCount();i++) 
                 {
                     tmp=(String)jcmbgroups.getItemAt(i);
                     if(tmp.equalsIgnoreCase(gid))
                     {
                         jcmbgroups.removeItemAt(i);
                         
                     }
                 }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"You are not a owner of this group, So You cannot delete it..!!!","Distributed Key",1);
        }
       }
       catch(Exception ex) 
       {
           ex.printStackTrace();
       }
    }//GEN-LAST:event_jbtndelgrpActionPerformed

