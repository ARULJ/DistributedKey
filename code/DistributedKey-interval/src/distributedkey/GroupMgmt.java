public boolean isalreadythere(Vector v,String s) 
 {
     Iterator itr=v.iterator();
     String stmp;
     boolean flag=false;
     while(itr.hasNext())
     {
	  stmp=(String)itr.next();
	  if(stmp.equalsIgnoreCase(s))
	  flag = true;
     } 
     return flag;
}
 public Vector getallitems(JComboBox jcmb)
 {
     Vector vtmp=new Vector(jcmb.getItemCount());
     for(int i=0;i<jcmb.getItemCount();i++)
         vtmp.add(jcmb.getItemAt(i));
     return vtmp;
 }
    private void jbtncreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtncreateActionPerformed
        // TODO add your handling code here: 
        try{
         if(jtxtgrpname.getText()==null)
         {
              JOptionPane.showMessageDialog(this,"Please enter group name..!!!","Distributed Key",0);
              return; 
         } //CREATE GROUP:groupname:ownerid
         
         String tg,td;
         cmd="CRTEGRP:"+jtxtgrpname.getText()+":"+me.getid();
         group g=new group(jtxtgrpname.getText(),me,s,nc,me);
         if(isalreadythere(getallitems(jcmbgroups),g.getid()))
         {
             JOptionPane.showMessageDialog(this,"Group already exists,Please enter another group name..!!!","Distributed Key",0);
             return;
         }    
         g.addmember(me.getid());
         g.startkeydaemon(me.getid());
         grps.addgroup(g);
         jcmbgroups.addItem(g.getid());// add to group management combo box
         jcmbgd.addItem(g.getid());//add to distributed combo box
         tg=(String)jcmbgroups.getSelectedItem();
         td=(String)jcmbgd.getSelectedItem();
         if(tg.equalsIgnoreCase(g.getid()))
             jlstmember.setListData(g.getvector());
         if(td.equalsIgnoreCase(g.getid()))
             jlstmd.setListData(g.getvector());
        synchronized(s)
        {
         s.sendmsg(cmd,false); 
        }
         JOptionPane.showMessageDialog(this,jtxtgrpname.getText()+" group is created..!!","Distributed Key",1);
         jtxtgrpname.setText("");
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }   
    }//GEN-LAST:event_jbtncreateActionPerformed

    private void jbtnjoinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnjoinActionPerformed
        // TODO add your handling code here:
        try{
         String tmp=(String)jcmbgroups.getSelectedItem();
         group g;
         synchronized(grps)
         {
          g=grps.getgroup(tmp);
          if(!g.ismember(me.getid()))
          {
            g.addmember(me); //JOIN GROUP
            grps.setgroup(tmp, g);
            grps.startkeydaemonforgroup(g.getid());  
          }
          else
          {
           JOptionPane.showMessageDialog(this,"You are already a member of the group "+tmp+" ..!!","Distributed Key",1);
           return;
          }
         } //JOIN:groupid:your id
        cmd="JOIN:"+g.getid()+":"+me.getid();
        s.sendmsg(cmd,false); 
        JOptionPane.showMessageDialog(this,"You have joined the group "+tmp+" ..!!","Distributed Key",1);       
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
            System.out.println(ex.toString());
        }  
        
    }//GEN-LAST:event_jbtnjoinActionPerformed

    private void jbtnleaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnleaveActionPerformed
        // TODO add your handling code here:
        try{
         String tmp=(String)jcmbgroups.getSelectedItem();
         group g;
         synchronized(grps)
         {
          g=grps.getgroup(tmp);
          if(g.getowner().getid().equalsIgnoreCase(me.getid()))
          {
              JOptionPane.showMessageDialog(this,"You are the owner of the group , you cannot leave the group ..!!","Distributed Key",1);
              return;
          }
          if(g.ismember(me.getid()))
          {
           synchronized(g){
           g.setkey(0);
           g.removemember(me); //LEAVE GROUP
           }
           g.stopkeydaemon();
           grps.setgroup(tmp, g);
          }
          else
          {
           JOptionPane.showMessageDialog(this,"You have already left the group or not a member of the group "+tmp+" ..!!","Distributed Key",1);    
           return;
          }
         } //LEAVE:groupid:your id
         cmd="LEAVE:"+g.getid()+":"+me.getid();
         s.sendmsg(cmd,false); 
         JOptionPane.showMessageDialog(this,"You have left the group "+tmp+" ..!!","Distributed Key",1);
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
            
        }  
       
    }//GEN-LAST:event_jbtnleaveActionPerformed

